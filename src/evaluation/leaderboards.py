def metrics_leaderboard(prep_data, **params):
    """
    This function will call the general models to be train with a maybe given set of params. Then, depending on the dict spitted out with :func:`prepare_data`.

    Args:
        prep_data (Dict): The dict generated by :func:`prepare_data`.

    Returns:
        leaderboard (DataFrame): A simple dataframe in the form of leaderboard with performance of models.
    """

    # Create empty rows to be append with info. Then throw basic decission tree.
    rows = []
    for name, (X_tr_p, X_te_p, _, cats, y_tr, y_te, _) in prep_data.items():
        if name in ("ordinal", "native"):
            trainer = Cat_Trainer(cat_features=cats, **params)

        else:
            trainer = LGBM_Trainer(**params)

        trainer.fit(X_tr_p, y_tr)  #  Fit

        # Safe unified importances → DataFrame → sort
        if isinstance(trainer, Cat_Trainer):
            raw_imps = trainer.model.get_feature_importance(prettified=False)  # Array [58.2, 12.4, ...]
            feat_names = [col for col in X_tr_p.columns] if cats else X_tr_p.columns.tolist()
            importances_dict = dict(zip(feat_names, raw_imps))
        else:  # LGBM
            raw_imps = trainer.median_model.feature_importances_
            importances_dict = dict(zip(X_tr_p.columns, raw_imps))

        # Sort safely
        imp_df = pd.DataFrame(list(importances_dict.items()), columns=['feat', 'imp'])
        imp_df = imp_df.sort_values('imp', ascending=False).reset_index(drop=True)
        features_relevance = ' | '.join([f"{row.feat}" for _, row in imp_df.iterrows()])

        preds = trainer.predict(X_te_p)  # then predict
        preds_train = trainer.predict(X_tr_p)
        # Values to appear in leaderboard (insights about the model)
        r2_test = r2_score(y_te, preds)
        r2_train = r2_score(y_tr, preds_train)
        cv_score = cross_val_score(trainer, X_tr_p, y_tr, cv=3, scoring='r2')
        cv_std = cv_score.std()
        rmse = np.sqrt(mean_squared_error(y_te, preds))
        mae = mean_absolute_error(y_te, preds)

        rows.append({"Model": name,
                     "R² (test)": f"{r2_test:.2f}",
                     "R² (train)": f"{r2_train:.2f}",
                     "MAE": f"{mae:.0f}",
                     "RSME": f"{rmse:.0f}",
                     "CV score": f"{cv_score.mean():.2f}",
                     "CV std": f"{cv_std:.3f}",
                     "Features Relevance": f"{features_relevance}"},
                    )

    pd.set_option('display.max_colwidth', None)
    return pd.DataFrame(rows).sort_values("R² (test)", ascending=False)


def stats_prediction(prepared_data, **params):
    """
    This function will predict the BST of all the pokemon you have feed in the dataframe that includes all you new generation pokemon. It will take the models to be fit, fit them and then predict your desired stats.

    Args:
        prepared_data (DataFrame): The already preprocessed data, including new_gen dictionary.
        params (optional): In case you wanna train your models with some given parameters, to fine-tune the values.

    Returns:
        rows (DataFrame): A simple dataframe including stats and uncertainty of each pokemon's BST for each model.
    """

    rows = []
    for name, (X_tr_p, _, gen_p, cats, y_tr, _, gen_names) in prepared_data.items():
        if name in ("ordinal", "native"):
            trainer = Cat_Trainer(cat_features=cats, **params)
            trainer.fit(X_tr_p, y_tr)
            vals, uncs = trainer.predict_unc(gen_p)
        else:
            trainer = LGBM_Trainer(**params)
            trainer.fit(X_tr_p, y_tr)
            vals, uncs = trainer.predict_unc(gen_p)

        poke_dict = {
            poke_name: f"{round(v)} ± {round(u)}"
            for poke_name, v, u in zip(gen_names, vals, uncs)
        }

        rows.append({"Model": name, **poke_dict})

    return pd.DataFrame(rows)
